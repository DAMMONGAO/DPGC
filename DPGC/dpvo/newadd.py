class linear_module(nn.Module):
    def __init__(self,
                 d_model,
                 nhead,
                 attention='linear',
                 iscoarse=False):
        super(linear_module, self).__init__()

        self.dim = d_model // nhead
        self.nhead = nhead

        # multi-head attention
        self.q_proj = nn.Linear(d_model, d_model, bias=False)
        self.k_proj = nn.Linear(d_model, d_model, bias=False)
        self.v_proj = nn.Linear(d_model, d_model, bias=False)
        self.attention = LinearAttention() if attention == 'linear' else FullAttention()
        self.merge = nn.Linear(d_model, d_model, bias=False)

        # feed-forward network
        if iscoarse == True:
            self.mlp = nn.Sequential(
                nn.Linear(d_model*2, d_model*4, bias=False),
                SpatialGate(d_model*2),
                nn.GELU(),
                nn.Linear(d_model*2, d_model, bias=False),
            )
        else:
            self.mlp = nn.Sequential(
                nn.Linear(d_model*2, d_model*4, bias=False),
                nn.GELU(),
                nn.Linear(d_model*4, d_model, bias=False),
            )

        # norm and dropout
        self.norm1 = nn.LayerNorm(d_model)
        self.norm2 = nn.LayerNorm(d_model)
        self.rotary_emb = Position_Encoding(dim=d_model)
        self.gamma=nn.Parameter(torch.randn((1, self.nhead, 1, 1)))

    def forward(self, x, source, x_mask=None, source_mask=None):
        """
        Args:
            x (torch.Tensor): [N, L, C]
            source (torch.Tensor): [N, S, C]
            x_mask (torch.Tensor): [N, L] (optional)
            source_mask (torch.Tensor): [N, S] (optional)
            
        """

        # import pdb; pdb.set_trace()

        bs = x.size(0)
        query, key, value = x, source, source

        # multi-head attention
        mixed_query_layer = self.q_proj(query)
        mixed_key_layer = self.k_proj(key)
        que_pe = self.rotary_emb(mixed_query_layer)
        key_pe = self.rotary_emb(mixed_key_layer)
        query = Position_Encoding.embed_pos(mixed_query_layer, que_pe).view(bs, -1, self.nhead, self.dim).permute(0, 2, 1, 3)   # [B H N C]
        key = Position_Encoding.embed_pos(mixed_key_layer, key_pe).view(bs, -1, self.nhead, self.dim).permute(0, 2, 3, 1)   # [B H C N]
        
        value = self.v_proj(value).view(bs, -1, self.nhead, self.dim).permute(0, 2, 1, 3)   # [B H N C]

        kv = torch.matmul(key, value) #bs,h,c,c
        kv_norm = XNorm(kv, self.gamma) #bs,h,c,c
        q_norm = XNorm(query, self.gamma) #bs, h, n, c
        message = torch.matmul(q_norm,kv_norm).permute(0, 2, 1, 3).contiguous()

        # message = self.attention(query, key, value, q_mask=x_mask, kv_mask=source_mask)  # [N, L, (H, D)]
        message = self.merge(message.view(bs, -1, self.nhead*self.dim))  # [N, L, C]
        message = self.norm1(message)

        # feed-forward network
        message = self.mlp(torch.cat([x, message], dim=2))
        message = self.norm2(message)

        return x + message

