class gating_multi_conv(nn.Module):
    def __init__(self,
                 d_model,
                 d_hidden_model,
                 norm_off=True,
                 mul=True,
                ):
        super(gating_multi_conv, self).__init__()
        
        self.norm_off = norm_off
        if self.norm_off:
            self.layernorm_que = nn.LayerNorm(d_model)
            self.layernorm_kv = nn.LayerNorm(d_model)
        self.conv1x1_que= nn.Conv2d(in_channels=d_model, out_channels=d_hidden_model, kernel_size=1, stride=1, padding=0)
        self.conv1x1_kv = nn.Conv2d(in_channels=d_model, out_channels=d_hidden_model, kernel_size=1, stride=1, padding=0)
        
        # 3x3 卷积支路
        self.branch_3x3 = nn.Conv2d(d_hidden_model, d_hidden_model, kernel_size=3, padding=1)
        
        # 5x5 卷积支路
        self.branch_5x5 = nn.Conv2d(d_hidden_model, d_hidden_model, kernel_size=5, padding=2)
        
        # 7x7 卷积支路
        self.branch_7x7 = nn.Conv2d(d_hidden_model, d_hidden_model, kernel_size=7, padding=3)
        
        # gating
        self.gating_conv = nn.Conv2d(d_hidden_model, 3, kernel_size=1)
        
        self.merge = nn.Conv2d(d_hidden_model, d_model, kernel_size=1)
        
        self.mul = mul 
        
    def forward(self, x):
        #
        b, n, c, h0, w0 = x.shape
        x_temp = x.reshape(-1, c, h0, w0)
        
        if self.norm_off:
            x_temp = x_temp.reshape(b*n, c, -1).permute(0,2,1) #bn hw c
            
            x_temp_que = self.layernorm_que(x_temp)
            x_temp_que = x_temp_que.permute(0,2,1).reshape(b*n, c, h0, w0).contiguous()
            
            x_temp_kv = self.layernorm_kv(x_temp)
            x_temp_kv = x_temp_kv.permute(0,2,1).reshape(b*n, c, h0, w0).contiguous()

        else:
            x_temp_que = x_temp
            x_temp_kv = x_temp
    
        #
        x_que = self.conv1x1_que(x_temp_que)
        x_kv = self.conv1x1_kv(x_temp_kv)
        
        #处理x kv 加上moe和gating机制
        out_3x3 = self.branch_3x3(x_kv)
        out_5x5 = self.branch_5x5(x_kv)
        out_7x7 = self.branch_7x7(x_kv)
        
        #通过x_que生成gate
        gating_weights = self.gating_conv(x_que) #n 3 h w
        
        # 对权重应用 Softmax 归一化
        gating_weights = F.softmax(gating_weights, dim=1)  # 归一化权重为 [batch_size, 3, H W]
        
        # 找到每个位置的最小得分和它的索引
        min_scores, min_indices = gating_weights.min(dim=1, keepdim=True)

        # gating_weights[gating_weights == min_scores] = 0    
        
        gating_weights_masked = gating_weights.clone()  # 复制原始的 gating_weights 张量
        gating_weights_masked[gating_weights_masked == min_scores] = 0  # 替换最小得分为零
        
        # output = out_3x3 * gating_weights[:, 0:1, :, :] + out_5x5 * gating_weights[:, 1:2, :, :] + out_7x7 * gating_weights[:, 2:3, :, :]
        output = out_3x3 * gating_weights_masked[:, 0:1, :, :] + out_5x5 * gating_weights_masked[:, 1:2, :, :] + out_7x7 * gating_weights_masked[:, 2:3, :, :]
        
        if self.mul:
            output = output * x_que
        
        output = self.merge(output + x_que)
        
        output = output.reshape(b, n, c, h0, w0).contiguous()
        
        return output
        
