class SoftAgg_moe(nn.Module):
    def __init__(self, dim=512, expand=True, topk_ratio=0.9):
        super(SoftAgg_moe, self).__init__()
        self.dim = dim
        self.expand = expand
        self.topk_ratio = topk_ratio  # 设置 topk 值
        self.f = nn.Linear(self.dim, self.dim)
        self.g = nn.Linear(self.dim, self.dim)
        self.h = nn.Linear(self.dim, self.dim)

    def forward(self, x, ix):
        _, jx = torch.unique(ix, return_inverse=True)
        
        # 计算 g(x) 并获取其在每组中的 Top-K 权重
        gx = self.g(x)  # g(x) 的形状为 [N, dim]
        
        w = torch_scatter.scatter_softmax(gx, jx, dim=1)  # 原始 softmax 权重 #torch.Size([1, 5120, 384])

        # 获取每个分组中的 Top-K 权重及其索引
        _,num_w,_ = w.shape
        _, topk_indices = torch.topk(w, int(self.topk_ratio*num_w), dim=1) #torch.Size([1, 4608, 384])
        
        # 使用 Top-K 权重对 f(x) 进行加权，并在每组中求和
        topk_mask = torch.zeros_like(w).scatter_(1, topk_indices, 1.0)  # 构建 Top-K 掩码
        
        w_topk = w * topk_mask  # 仅保留 Top-K 权重 #torch.Size([1, 5120, 384])
        # import pdb; pdb.set_trace()
