        # print("energy_map min:", energy_map.min().item(), "max:", energy_map.max().item())
        energy_map = torch.clamp(energy_map, min=-10, max=10)
        weight_choumi = self.fc_weight(energy_map) #bnhw 1 
        weight_choumi = weight_choumi.reshape(b*n, h*w, 1)
        weight_xishu = 1 - weight_choumi
        return weight_choumi, weight_xishu
        
