def get_window_indices(n, h, w, k):
    # 获取特征图的尺寸： (n, c, h, w)
    # n, _, h, w = features.shape  # 去掉 c 维度，只考虑一个通道
    
    # 生成原始位置的坐标网格
    y = torch.arange(h).view(1, h, 1).expand(n, h, w)  # 高度索引
    x = torch.arange(w).view(1, 1, w).expand(n, h, w)  # 宽度索引
    
    # 生成窗口偏移量的网格 (k x k)
    offset = torch.arange(k) - k // 2  # 偏移量从 -k//2 到 k//2
    offset_y, offset_x = torch.meshgrid(offset, offset, indexing='ij')
    
    # 扩展 offset_y 和 offset_x 到与原图大小匹配
    offset_y = offset_y.view(1, 1, 1, k, k).expand(n, h, w, k, k)
    offset_x = offset_x.view(1, 1, 1, k, k).expand(n, h, w, k, k)
    
    # 扩展 y 和 x 到与窗口大小相同的形状
    y_expanded = y.view(n, h, w, 1, 1).expand(n, h, w, k, k)
    x_expanded = x.view(n, h, w, 1, 1).expand(n, h, w, k, k)
    
    # 计算每个位置的窗口索引
    window_y = (y_expanded + offset_y).clamp(min=0, max=h-1)  # 限制在有效范围内
    window_x = (x_expanded + offset_x).clamp(min=0, max=w-1)  # 限制在有效范围内
    
    # 将 (y, x) 转换为一维索引
    window_indices = window_y * w + window_x  # 转换为一维索引
    
    # 重塑成目标形状 (n, h*w, k*k)
    window_indices = window_indices.view(n, h * w, k * k)
    
    return window_indices

def create_mask(window_indices, h, w):
    n, hw, kk = window_indices.shape  # (n, h*w, k*k)
    
    # 构建一个全为0的mask，大小为 (n, h*w, h*w)
    # import pdb; pdb.set_trace()
    mask = torch.zeros((n, hw, hw), dtype=torch.float32)
    
    # 使用高级索引构建 mask
    # 展开 window_indices 成为 (n, hw, k*k) 的索引位置
    mask = mask.scatter_(2, window_indices, 1)  # 将窗口索引对应的值设为1
    #(n, h*w, h*w)
    return mask

class FullAttention(Module):
    def __init__(self, k1, k2, use_dropout=False, attention_dropout=0.1):
        super().__init__()
        self.use_dropout = use_dropout
        self.dropout = Dropout(attention_dropout)
        self.k1 = k1
        self.k2 = k2
    
    def forward(self, queries, keys, value_local, values, n, h, w, q_mask=None, kv_mask=None):
        """ Multi-head scaled dot-product attention, a.k.a full attention.
        Args:
            queries: [N, L, H, D]
            keys: [N, S, H, D]
            values: [N, S, H, D]
            q_mask: [N, L]
            kv_mask: [N, S]
            
            qk_local_mask: [N, L, S]
        Returns:
            queried_values: (N, L, H, D)
        """
        # Compute the unnormalized attention and apply the masks
        QK = torch.einsum("nlhd,nshd->nlsh", queries, keys)
        if kv_mask is not None:
            QK.masked_fill_(~(q_mask[:, :, None, None] * kv_mask[:, None, :, None]), float('-inf'))

        softmax_temp = 1. / queries.size(3)**.5  # sqrt(D)
        
        if h is not None:
            k1, k2 = self.k1, self.k2
