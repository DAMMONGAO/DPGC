def _trunc_normal_(tensor, mean, std, a, b):
    # Cut & paste from PyTorch official master until it's in a few official releases - RW
    # Method based on https://people.sc.fsu.edu/~jburkardt/presentations/truncated_normal.pdf
    def norm_cdf(x):
        # Computes standard normal cumulative distribution function
        return (1. + math.erf(x / math.sqrt(2.))) / 2.

    if (mean < a - 2 * std) or (mean > b + 2 * std):
        warnings.warn("mean is more than 2 std from [a, b] in nn.init.trunc_normal_. "
                      "The distribution of values may be incorrect.",
                      stacklevel=2)

    # Values are generated by using a truncated uniform distribution and
    # then using the inverse CDF for the normal distribution.
    # Get upper and lower cdf values
    l = norm_cdf((a - mean) / std)
    u = norm_cdf((b - mean) / std)

    # Uniformly fill tensor with values from [l, u], then translate to
    # [2l-1, 2u-1].
    tensor.uniform_(2 * l - 1, 2 * u - 1)

    # Use inverse cdf transform for normal distribution to get truncated
    # standard normal
    tensor.erfinv_()

    # Transform to proper mean, std
    tensor.mul_(std * math.sqrt(2.))
    tensor.add_(mean)

    # Clamp to ensure it's in the proper range
    tensor.clamp_(min=a, max=b)
    return tensor

def trunc_normal_(tensor, mean=0., std=1., a=-2., b=2.):
    # type: (Tensor, float, float, float, float) -> Tensor
    r"""Fills the input Tensor with values drawn from a truncated
    normal distribution. The values are effectively drawn from the
    normal distribution :math:`\mathcal{N}(\text{mean}, \text{std}^2)`
    with values outside :math:`[a, b]` redrawn until they are within
    the bounds. The method used for generating the random values works
    best when :math:`a \leq \text{mean} \leq b`.

    NOTE: this impl is similar to the PyTorch trunc_normal_, the bounds [a, b] are
    applied while sampling the normal with mean/std applied, therefore a, b args
    should be adjusted to match the range of mean, std args.

    Args:
        tensor: an n-dimensional `torch.Tensor`
        mean: the mean of the normal distribution
        std: the standard deviation of the normal distribution
        a: the minimum cutoff value
        b: the maximum cutoff value
    Examples:
        >>> w = torch.empty(3, 5)
        >>> nn.init.trunc_normal_(w)
    """
    with torch.no_grad():
        return _trunc_normal_(tensor, mean, std, a, b)

def drop_path(x, drop_prob: float = 0., training: bool = False, scale_by_keep: bool = True):
    """Drop paths (Stochastic Depth) per sample (when applied in main path of residual blocks).

    This is the same as the DropConnect impl I created for EfficientNet, etc networks, however,
    the original name is misleading as 'Drop Connect' is a different form of dropout in a separate paper...
    See discussion: https://github.com/tensorflow/tpu/issues/494#issuecomment-532968956 ... I've opted for
    changing the layer and argument names to 'drop path' rather than mix DropConnect as a layer name and use
    'survival rate' as the argument.

    """
    if drop_prob == 0. or not training:
        return x
    keep_prob = 1 - drop_prob
    shape = (x.shape[0],) + (1,) * (x.ndim - 1)  # work with diff dim tensors, not just 2D ConvNets
    random_tensor = x.new_empty(shape).bernoulli_(keep_prob)
    if keep_prob > 0.0 and scale_by_keep:
        random_tensor.div_(keep_prob)
    return x * random_tensor

class DropPath(nn.Module):
    """Drop paths (Stochastic Depth) per sample  (when applied in main path of residual blocks).
    """
    def __init__(self, drop_prob: float = 0., scale_by_keep: bool = True):
        super(DropPath, self).__init__()
        self.drop_prob = drop_prob
        self.scale_by_keep = scale_by_keep

    def forward(self, x):
        return drop_path(x, self.drop_prob, self.training, self.scale_by_keep)

    def extra_repr(self):
        return f'drop_prob={round(self.drop_prob,3):0.3f}'

class Mlp(nn.Module):
    def __init__(self, in_features, hidden_features=None, out_features=None, act_layer=nn.GELU, drop=0., linear=False):
        super().__init__()
        out_features = out_features or in_features
        hidden_features = hidden_features or in_features
        self.fc1 = nn.Linear(in_features, hidden_features)
        self.act = act_layer()
        self.fc2 = nn.Linear(hidden_features, out_features)
        self.drop = nn.Dropout(drop)
        self.linear = linear
        if self.linear:
            self.relu = nn.ReLU(inplace=True)
        self.apply(self._init_weights)

    def _init_weights(self, m):
        if isinstance(m, nn.Linear):
            trunc_normal_(m.weight, std=.02)
            if isinstance(m, nn.Linear) and m.bias is not None:
                nn.init.constant_(m.bias, 0)
        elif isinstance(m, nn.LayerNorm):
            nn.init.constant_(m.bias, 0)
            nn.init.constant_(m.weight, 1.0)
        elif isinstance(m, nn.Conv2d):
            fan_out = m.kernel_size[0] * m.kernel_size[1] * m.out_channels
            fan_out //= m.groups
            m.weight.data.normal_(0, math.sqrt(2.0 / fan_out))
            if m.bias is not None:
                m.bias.data.zero_()

    def forward(self, x):
        x = self.fc1(x)
        if self.linear:
            x = self.relu(x)
        x = self.act(x)
        x = self.drop(x)
        x = self.fc2(x)
        x = self.drop(x)
        return x

class linearattention(nn.Module):
    def __init__(self, embed_dim, num_heads, fr_ratio=1, se_reduction=2):
        super().__init__()
        self.embed_dim = embed_dim
        self.num_heads = num_heads
		# q, k, v projection
        self.fr = fr_ratio 
        # feature reduction
        self.k_proj = nn.Linear(embed_dim, embed_dim // self.fr)
        self.v_proj = nn.Linear(embed_dim, embed_dim // self.fr)
        self.q_proj = nn.Linear(embed_dim, embed_dim // self.fr)
        
        self.apply(self._init_weights)
        # outprojection
        self.out_proj = nn.Linear(embed_dim//self.fr, embed_dim)
        
        # se block:
        reduction = se_reduction
        self.se_pool = nn.AdaptiveAvgPool1d(1)
        self.se_fc = nn.Sequential(
            nn.Linear(embed_dim, embed_dim // reduction, bias=False),
            nn.ReLU(inplace=True),
            nn.Linear(embed_dim // reduction, embed_dim, bias=False),
            nn.Sigmoid()
        )
        
        self.clip = True
        
    def _init_weights(self, m):
        if isinstance(m, nn.Linear):
            trunc_normal_(m.weight, std=.02)
            if isinstance(m, nn.Linear) and m.bias is not None:
                nn.init.constant_(m.bias, 0)
        elif isinstance(m, nn.LayerNorm):
            nn.init.constant_(m.bias, 0)
            nn.init.constant_(m.weight, 1.0)
        elif isinstance(m, nn.Conv2d):
            fan_out = m.kernel_size[0] * m.kernel_size[1] * m.out_channels
            fan_out //= m.groups
            m.weight.data.normal_(0, math.sqrt(2.0 / fan_out))
            if m.bias is not None:
                m.bias.data.zero_()
    
    def abs_clamp(self, t):
        min_mag = 1e-4
        max_mag = 10000
        sign = t.sign()
        return t.abs_().clamp_(min_mag, max_mag)*sign
        
    def forward(self, query):
        num_heads = self.num_heads
        B, N, C = query.shape
        
        query_se = query.permute(0, 2, 1)
        query_se = self.se_pool(query_se).view(B, C)
        query_se = self.se_fc(query_se).view(B, C, 1)
        
        k =  self.k_proj(query)
        v = self.v_proj(query)
        
        k = k.permute(1, 0, 2)
        v = v.permute(1, 0, 2)
        
        query = query.permute(1,0,2)
        tgt_len, bsz, embed_dim = query.size()
        head_dim = embed_dim // num_heads
        # (L, N, E)
        q = self.q_proj(query)
        
 		# multihead
		# (N, L, h, d)
        q = q.contiguous().view(tgt_len, bsz, num_heads, head_dim//self.fr).transpose(0, 1)
		# (N, S, h, d)
        k = k.contiguous().view(-1, bsz, num_heads, head_dim//self.fr).transpose(0, 1)
		# (N, S, h, d)
        v = v.contiguous().view(-1, bsz, num_heads, head_dim//self.fr).transpose(0, 1)
		# relu
        q = F.relu(q)
        k = F.relu(k)
        
        eps = 1e-4
        kv_ =  torch.matmul(k.permute(0, 2, 3, 1), v.permute(0, 2, 1, 3))  
        if self.clip:
            kv_ = self.abs_clamp(kv_)
            
        k_sum = torch.sum(k, axis=1, keepdim=True) # no einsum                         
        z_ = 1 / (torch.sum(torch.mul(q, k_sum), axis=-1) + eps) # no einsum           
        if self.clip:
            z_ = self.abs_clamp(z_)
            
        attn_output = torch.matmul(q.transpose(1, 2), kv_).transpose(1, 2)
        if self.clip:
            attn_output = self.abs_clamp(attn_output)
        
        # nlhm,nlh -> nlhm
        attn_output = torch.mul(attn_output, z_.unsqueeze(-1))
        if self.clip:
            attn_output = self.abs_clamp(attn_output)
            
        # (N, L, h, d) -> (L, N, h, d) -> (L, N, E)
        attn_output = attn_output.transpose(0, 1).contiguous().view(tgt_len, bsz, embed_dim//self.fr)
        
        attn_output = self.out_proj(attn_output)
        if self.clip:
            attn_output = self.abs_clamp(attn_output)

        # ------------------------------------- se block
        attn_output = attn_output.permute(1,2,0)
        attn_output = attn_output + attn_output * query_se.expand_as(attn_output)
        if self.clip:
            attn_output = self.abs_clamp(attn_output)
        # import pdb; pdb.set_trace()
        attn_output = attn_output.permute(2,0,1)
        # -------------------------------------------------

        attn_output = attn_output.permute(1,0,2)

        return attn_output

class transformer_block(nn.Module):
    def __init__(self, dim, num_heads=4, mlp_ratio=2., drop=0., drop_path=0., act_layer=nn.GELU, norm_layer=nn.LayerNorm, fr_ratio = 1, linear=False, se_ratio=1):
        super().__init__()
        self.norm1 = norm_layer(dim)
        self.attn = linearattention(embed_dim=dim, num_heads=num_heads, fr_ratio = fr_ratio, se_reduction=se_ratio)
        
        self.drop_path = DropPath(drop_path) if drop_path > 0. else nn.Identity()
        self.norm2 = norm_layer(dim)
        
        mlp_hidden_dim = int(dim * mlp_ratio)
        self.mlp = Mlp(in_features=dim, hidden_features=mlp_hidden_dim, act_layer=act_layer, drop=drop, linear=linear)
        self.apply(self._init_weights)
        
    def _init_weights(self, m):
        if isinstance(m, nn.Linear):
            trunc_normal_(m.weight, std=.02)
            if isinstance(m, nn.Linear) and m.bias is not None:
                nn.init.constant_(m.bias, 0)
        elif isinstance(m, nn.LayerNorm):
            nn.init.constant_(m.bias, 0)
            nn.init.constant_(m.weight, 1.0)
        elif isinstance(m, nn.Conv2d):
            fan_out = m.kernel_size[0] * m.kernel_size[1] * m.out_channels
            fan_out //= m.groups
            m.weight.data.normal_(0, math.sqrt(2.0 / fan_out))
            if m.bias is not None:
                m.bias.data.zero_()
    
    def forward(self, x):
        x = x + self.drop_path(self.attn(self.norm1(x)))
        x = x + self.drop_path(self.mlp(self.norm2(x)))

        return x

